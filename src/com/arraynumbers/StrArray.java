package com.arraynumbers;

public class StrArray {
	
	public static void main(String[] args) {
	
	String[] city = new String[4];  
	  
	city[0] = "Delhi";  
	city[1] = "Jaipur";  
	city[2] = "Gujarat";  
	city[3] = "Mumbai";  
	 
	for (String str : city)   
	{  
	System.out.println(str);  
	}  
	}  
	}  

	
